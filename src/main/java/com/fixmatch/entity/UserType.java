package com.fixmatch.entity;

/**
 * UserType Enum - Defines the types of users in the system
 * 
 * - CLIENT: Users who post jobs and hire service providers
 * - PROVIDER: Service providers who offer their skills
 * - BOTH: Users who can be both clients and providers
 */
public enum UserType {
    CLIENT,
    PROVIDER,
    BOTH
}
